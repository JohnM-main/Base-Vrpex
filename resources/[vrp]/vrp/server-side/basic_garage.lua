-- GERAR PLACA
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function vRP.gerarPlaca()
    local checkPlate
	local plate
	repeat
		plate = generateStringNumber("LLLDDDD")
		checkPlate = vRP.getUserByPlate(plate)
	until not checkPlate

    return plate
end 
 
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- CAPTURAR ID PELA PLACA
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local mPlaca = {}
mPlaca['getUserByPlate'] = {}

function vRP.getUserByPlate(plate)
	if mPlaca['getUserByPlate'][plate] == nil then
		local rows = vRP.query("vRP/get_plate_id", {placa = plate or ""})
		if #rows > 0 then
			mPlaca['getUserByPlate'][plate] = parseInt(rows[1].user_id)
			return mPlaca['getUserByPlate'][plate]
		end
	else
		return mPlaca['getUserByPlate'][plate]
	end
	
	return false
end

function vRP.updatePlate(plate)
    if plate then
        local plate = string.gsub(plate, " ","")
        if plate then
            mPlaca['getUserByPlate'][plate] = nil
        end
    end
end

function vRP.updateAllPlates(plate)
    mPlaca['getUserByPlate'] = {}
end

